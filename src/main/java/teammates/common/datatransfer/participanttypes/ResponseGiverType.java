package teammates.common.datatransfer.participanttypes;

/**
 * Represents the type of a response giver for a feedback response or comment.
 */
public enum ResponseGiverType {
    /**
     * Response is given by a team.
     */
    TEAM,
    /**
     * Response is given by an individual student.
     */
    STUDENT,
    /**
     * Response is given by an instructor.
     */
    INSTRUCTOR,
}
