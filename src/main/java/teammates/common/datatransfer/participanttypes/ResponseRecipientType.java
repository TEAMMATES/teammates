package teammates.common.datatransfer.participanttypes;

/**
 * Represents the type of recipient that received a feedback response.
 */
public enum ResponseRecipientType {
    /**
     * Represents a student recipient.
     */
    STUDENT,

    /**
     * Represents an instructor recipient.
     */
    INSTRUCTOR,

    /**
     * Represents a team recipient.
     */
    TEAM,

    /**
     * Represents no specific recipient.
     */
    NO_SPECIFIC_RECIPIENT
}
